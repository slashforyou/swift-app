/**
 * Extraire l'ID num√©rique d'un job code
 * Exemples:
 * - "JOB-DEC-002" ‚Üí "2"
 * - "2" ‚Üí "2"
 * - "JOB-NERD-PENDING-042" ‚Üí "42"
 */
function extractNumericId(jobCode: string): string {
  // Si d√©j√† num√©rique, retourner tel quel
  if (/^\d+$/.test(jobCode)) {
    return jobCode;
  }
  
  // Extraire le dernier groupe de chiffres
  const match = jobCode.match(/(\d+)$/);
  if (match) {
    // Convertir en nombre puis string pour enlever les z√©ros de t√™te
    return String(parseInt(match[1], 10));
  }
  
  // Si aucun nombre trouv√©, retourner tel quel (le serveur g√©rera l'erreur)
  return jobCode;
}

/**
 * R√©cup√©rer le token d'authentification
 */
async function getAuthToken(): Promise<string> {
  try {
    const AsyncStorage = require('@react-native-async-storage/async-storage').default;
    const token = await AsyncStorage.getItem('@auth_token');
    return token || '';
  } catch (error) {
    console.error('‚ùå [JobCorrection] Failed to get auth token:', error);
    return '';
  }
}

/**
 * R√©cup√©rer la version de l'app
 */
function getAppVersion(): string {
  try {
    const packageJson = require('../../package.json');
    return packageJson.version || '1.0.0';
  } catch (error) {
    return '1.0.0';
  }
}

/**
 * R√©cup√©rer la plateforme
 */
function getPlatform(): string {
  try {
    const { Platform } = require('react-native');
    return Platform.OS; // 'ios' ou 'android'
  } catch (error) {
    return 'unknown';
  }
}

